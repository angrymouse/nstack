#!/usr/bin/env node
import { dirname } from "node:path";
import { mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";

const [metadataFile, entrypointFile, outputFile] = process.argv.slice(2);
if (!metadataFile || !entrypointFile || !outputFile) {
  console.error("Usage: node scripts/nstack-cron-runner.mjs <metadata.json> <combined-main.ts> <output.ts>");
  process.exit(64);
}

const meta = JSON.parse(readFileSync(metadataFile, "utf8"));
const output = buildCronRunnerSource({
  meta,
  entrypointSource: readFileSync(entrypointFile, "utf8"),
});

mkdirSync(dirname(outputFile), { recursive: true });
if (!output) {
  rmSync(outputFile, { force: true });
  process.exit(0);
}
writeFileSync(outputFile, output);

function buildCronRunnerSource({ meta, entrypointSource }) {
  const cronJobs = Array.isArray(meta?.cron_jobs) ? meta.cron_jobs : [];
  if (cronJobs.length === 0) return "";

  const imports = parseEntrypointImports(entrypointSource);
  const routes = parseEntrypointRoutes(entrypointSource);
  const importLines = [];
  const handlerLines = [];

  for (const [index, cron] of cronJobs.entries()) {
    const endpoint = cronEndpoint(meta, cron.endpoint);
    if (!endpoint) throw new Error(`Encore cron ${cron.id || "(unknown)"} does not reference a resolvable endpoint.`);

    const route = routes.get(routeKey(endpoint.service, endpoint.name));
    if (!route) throw new Error(`Encore cron ${cron.id} endpoint ${endpoint.service}.${endpoint.name} was not found in the generated combined entrypoint.`);

    const imported = imports.get(route.handler);
    if (!imported) throw new Error(`Encore cron ${cron.id} handler ${route.handler} was not found in the generated combined entrypoint imports.`);

    const alias = `nstackCron${index}`;
    importLines.push(`import { ${imported.imported} as ${alias} } from ${JSON.stringify(imported.specifier)};`);
    handlerLines.push(`  [${JSON.stringify(cron.id)}, ${alias}],`);
  }

  return `${importLines.join("\n")}

// Generated by nstack. This is bundled into the backend image for Dokploy schedules.
type CronHandler = () => unknown | Promise<unknown>;

const handlers = new Map<string, CronHandler>([
${handlerLines.join("\n")}
]);

async function main() {
  const name = process.argv[2] || "";
  const handler = handlers.get(name);
  if (!name || !handler) {
    const available = [...handlers.keys()].join(", ") || "(none)";
    console.error(\`Unknown Encore cron "\${name || "(missing)"}". Available crons: \${available}\`);
    process.exit(64);
  }

  await handler();
  await drainAsyncRuntime();
  process.exit(0);
}

async function drainAsyncRuntime() {
  const delay = Number(process.env.NSTACK_CRON_DRAIN_MS || "5000");
  if (!Number.isFinite(delay) || delay <= 0) return;
  await new Promise((resolve) => setTimeout(resolve, delay));
}

main().catch((error) => {
  console.error(error?.stack || error?.message || String(error));
  process.exit(1);
});
`;
}

function parseEntrypointImports(source) {
  const imports = new Map();
  const pattern = /import\s+\{\s*([A-Za-z_$][\w$]*)\s+as\s+([A-Za-z_$][\w$]*)\s*\}\s+from\s+["']([^"']+)["'];/g;
  for (const match of source.matchAll(pattern)) {
    imports.set(match[2], { imported: match[1], specifier: match[3] });
  }
  return imports;
}

function parseEntrypointRoutes(source) {
  const routes = new Map();
  const pattern = /apiRoute:\s*\{([\s\S]*?)\}\s*,\s*endpointOptions:/g;
  for (const match of source.matchAll(pattern)) {
    const block = match[1];
    const service = propertyString(block, "service");
    const name = propertyString(block, "name");
    const handler = propertyIdentifier(block, "handler");
    if (service && name && handler) routes.set(routeKey(service, name), { service, name, handler });
  }
  return routes;
}

function cronEndpoint(meta, qualifiedName) {
  if (!qualifiedName) return null;
  for (const service of meta.svcs || []) {
    if (service.rel_path !== qualifiedName.pkg && service.name !== qualifiedName.pkg) continue;
    const rpc = (service.rpcs || []).find((entry) => entry.name === qualifiedName.name);
    if (rpc) return { service: service.name, name: rpc.name };
  }
  return { service: qualifiedName.pkg, name: qualifiedName.name };
}

function propertyString(source, name) {
  return source.match(new RegExp(`\\b${name}\\s*:\\s*"([^"]+)"`))?.[1] || "";
}

function propertyIdentifier(source, name) {
  return source.match(new RegExp(`\\b${name}\\s*:\\s*([A-Za-z_$][\\w$]*)`))?.[1] || "";
}

function routeKey(service, name) {
  return `${service}.${name}`;
}
